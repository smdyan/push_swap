char	*ft_strrchr(const char *s, int c)

{
	int		i;
	char	*p;
	char	ch;

	i = 0;
	p = 0;
	ch = (char)c;
	while (*(s + i) != '\0')
	{
		if (*(s + i) == ch)
			p = (char *)(s + i);
		i++;
	}
	if (*(s + i) == ch)
		p = (char *)(s + i);
	return (p);
}
