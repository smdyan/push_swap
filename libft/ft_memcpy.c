#include "libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	size_t			i;
	unsigned char	*dst2;
	unsigned char	*src2;

	i = 0;
	dst2 = (unsigned char *)dst;
	src2 = (unsigned char *)src;
	if (n == 0 || dst == src)
		return (dst);
	while (i != n)
	{
		*(dst2 + i) = *(src2 + i);
		i++;
	}
	return (dst);
}
