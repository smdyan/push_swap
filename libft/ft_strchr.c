char	*ft_strchr(const char *s, int c)

{
	int		i;
	char	*p;
	char	ch;

	i = 0;
	p = 0;
	ch = (char)c;
	while (*(s + i) != ch)
	{
		if (*(s + i) == '\0')
			return (p);
		i++;
	}
	p = (char *)(s + i);
	return (p);
}
